                imageContainer.style.zIndex = '99999';

                // Tạo phần tử hình ảnh lớn và đặt các thuộc tính CSS
                var largeImage = document.createElement('img');
                largeImage.src = element.src.replace('small/', 'large/'); // thay đổi đường dẫn ảnh nhỏ thành ảnh lớn
                largeImage.style.display = 'block';
                largeImage.style.maxHeight = '80%';
