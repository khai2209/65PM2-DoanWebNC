                    var delta = event.deltaY || event.detail || event.wheelDelta;
                    if (delta < 0) {
                        if (imageScale < 2) imageScale += 0.1;
                    } else {
                        if (imageScale > 1) imageScale -= 0.1;
                    }
                    largeImage.style.transform = 'translate(-50%, -50%) scale(' + imageScale + ')';
                });

                // Xóa phần tử div lớn khi người dùng click vào nó
                imageContainer.onclick = function() {
                    document.body.removeChild(imageContainer);
                };
