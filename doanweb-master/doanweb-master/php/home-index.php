                largeImage.style.top = '50%';
                largeImage.style.left = '50%';
                largeImage.style.transform = 'translate(-50%, -50%) scale(1)';

                // Thêm phần tử hình ảnh lớn vào phần tử div lớn
                imageContainer.appendChild(largeImage);

                // Thêm phần tử div lớn vào trang web
                document.body.appendChild(imageContainer);

